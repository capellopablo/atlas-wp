/**
 * Generates options for the dropdown.
 * 
 * @param {Array} currentList - The current list of AOIs.
 * @param {Array} areasOfInterest - The areas of interest to match against.
 * @returns {Array} The generated options.
*/
export const generateOptions = (currentList, areasOfInterest) => {
	// Check if currentList or areasOfInterest are not provided or are empty
	if (!currentList || currentList.length === 0 || !areasOfInterest || areasOfInterest.length === 0) {
		return [];
	}

	return currentList.map(option => {
		const matchedArea = areasOfInterest.find(area => area.value === option);
		
		if (matchedArea && matchedArea?.name && matchedArea?.value) {
			return {
				item: matchedArea.name,
				value: matchedArea.value,
			};
		}
		return null;
	}).filter(option => option !== null);
};

/**
 * Gets the default value for the dropdown.
 * 
 * @param {Object} selectedOption - The selected option.
 * @param {Object} values - The form values.
 * @param {Array} areasOfInterestList - The list of areas of interest.
 * @returns {string} The default value.
 */
export const getDefaultValue = (selectedOption, values, areasOfInterestList) => {
	if (selectedOption) {
		return selectedOption.item;
	}

	if (!areasOfInterestList || areasOfInterestList.length === 0) {
		return;
	}

	const matchedArea = areasOfInterestList?.find(area => area.value === values.area_of_interest);
	if (matchedArea) {
		return matchedArea.name;
	}
	
	return '';
};

/**
 * Gets the school of interest value based on the current area of interest.
 * 
 * @param {string} currentAOI - The current area of interest.
 * @returns {string} - The school of interest value.
 */
export const getSchoolOfInterestValue = (currentAOI) => {
	if (currentAOI !== window?.aprende21?.tracking?.areaOfInterest) {
		return areasOfInterestList?.find(area => area.value === currentAOI)?.tracking?.schoolOfInterest;
	}
	return window?.aprende21?.tracking?.schoolOfInterest;
};

/**
 * Handles the select event for the dropdown.
 * 
 * @param {Event} e - The event object.
 * @param {Object} item - The selected item.
 * @param {Object} formSettings - Form settings.
 * @param {Function} setFieldValue - Function to set form field value.
 * @param {Function} setAmplitudeEventChanged - Function to set Amplitude event.
 * @param {string} currentAOI - The current area of interest.
 */
export const handleSelect = (e, item, formSettings, setFieldValue, setAmplitudeEventChanged, currentAOI) => {
	let currentSchoolOfInterest = getSchoolOfInterestValue(currentAOI);

	if (currentSchoolOfInterest) {
		const renameLegacySchool = {
			'Gastronomía': 'gastronomica',
			'Hospitalidad y Eventos': 'hospitalidad',
		};

		const schoolOfInterestValue = renameLegacySchool[currentSchoolOfInterest] || currentSchoolOfInterest?.toLowerCase();
		setFieldValue('School_of_interest', schoolOfInterestValue);
	}

	setAmplitudeEventChanged(e.target, {
		type: 'select',
		value: item.value,
		label: formSettings.area_of_interest_label,
	});
};
