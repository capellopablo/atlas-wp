const getTimezoneName = (timezoneMapItem) => {
  // eslint-disable-next-line no-prototype-builtins
  if (! timezoneMapItem || ! timezoneMapItem.hasOwnProperty('abbr')) {
    return null;
  }

  const abbrToNameMap = {
    'AKST': 'America/Anchorage',
    'AST': 'America/Dominica',
    'CST': 'America/Chicago',
    'EST': 'America/New_York',
    'HAST': 'Pacific/Honolulu',
    'MST': (timezoneMapItem.state === 'Arizona') ? 'America/Phoenix' : 'America/Denver',
  };

  return abbrToNameMap[timezoneMapItem.abbr];
};

const getQueryStringValue = (arg) => {
	let result = null,
		tmp = [];

	location.search
		.substr(1)
		.split('&')
		.forEach(function (item) {
			tmp = item.split('=');
			if (tmp[0] === arg) {
				result = decodeURIComponent(tmp[1]);
			}
		});

	return result;
};

const isUSPhone = (phone) => {
	const regex = /^([+]?1[\s]?)?((?:[(](?:[2-9]1[02-9]|[2-9][02-8][0-9])[)][\s]?)|(?:(?:[2-9]1[02-9]|[2-9][02-8][0-9])[\s.-]?)){1}([2-9]1[02-9]|[2-9][02-9]1|[2-9][02-9]{2}[\s.-]?){1}([0-9]{4}){1}$/;
	return regex.test(phone);
};

const getAreaCode = (phone) => {
	return isUSPhone(phone) ? phone.replace(/\D/g, '').substr(1, 3) : '';
};
