const needsNightLeadConsent = (phone) => {
	const timestamp = getQueryStringValue('timestamp'), // Allow forcing timestamp through query string.
		localDate = timestamp ? new Date(parseInt(timestamp)) : new Date(),
		targetTimezones = ['AST', 'EST', 'MST', 'CST'],
		areaCode = getAreaCode(phone),
		timezone = getTimezoneFromAreaCode(areaCode),
    timezoneAbbr = timezone ? timezone.abbr : '',
		timezoneName = timezone ? getTimezoneName(timezone) : null,
		militaryTime = {
			current: timezoneName
				? parseInt(localDate.toLocaleString('en-US', {
					timeZone: timezoneName,
					hour12: false,
					hour: '2-digit',
					minute: '2-digit',
				}).replace(':', ''))
				: 0,
			min: 2100,
			max: 2254,
		};

	if (! targetTimezones.includes(timezoneAbbr)) {
		return false;
	}

	// noinspection RedundantIfStatementJS
	if (militaryTime.current < militaryTime.min || militaryTime.current > militaryTime.max) {
		return false;
	}

	return true;
};

export default needsNightLeadConsent;

